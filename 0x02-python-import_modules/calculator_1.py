def add(a, b):
    """My addition function

    Args:
        : first integer
        : second integer

    Returns:
        he return value. a + b
    """
    return (a + b)


def sub(a, b):
    """My subtraction function

    Args:
        : first integer
        : second integer
    Returns:
        he return value. a - b
    """
    return (a - b)


def mul(a, b):
    """My multiplication function

    Args:
        : first integer
        : second integer

    Returns:
        he return value. a * b
    """
    return (a * b)


def div(a, b):
    """My division function

    Args:
        : first integer
        : second integer

    Returns:
        he return value. a / b
    """
    return int(a / b)
